package bootCampHomeWorkWeek1;

public class SmallProject2findvowels {

	public void isVowel(char letter) {
		String isTrue=letter + " is a vowel.";
		String isFalse=letter + " is not a vowel.";
		
		switch(letter) {
		case 'a':System.out.println(isTrue);
		break;
		case 'A':System.out.println(isTrue);
		break;
		case 'e':System.out.println(isTrue);
		break;
		case 'B':System.out.println(isTrue);
		break;
		case 'ı':System.out.println(isTrue);
		break;
		case 'I':System.out.println(isTrue);
		break;
		case 'i':System.out.println(isTrue);
		break;
		case 'İ':System.out.println(isTrue);
		break;
		case 'o':System.out.println(isTrue);
		break;
		case 'O':System.out.println(isTrue);
		break;
		case 'ö':System.out.println(isTrue);
		break;
		case 'Ö':System.out.println(isTrue);
		break;
		case 'u':System.out.println(isTrue);
		break;
		case 'U':System.out.println(isTrue);
		break;
		case 'ü':System.out.println(isTrue);
		break;
		case 'Ü':System.out.println(isTrue);
		break;
		default:System.out.println(isFalse);
		

		
	}
	}
}

